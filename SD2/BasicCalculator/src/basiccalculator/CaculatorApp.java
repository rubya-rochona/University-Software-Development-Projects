    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        
        // code for backspace button
        
        int length = jTextField1.getText().length();    // returns the length of the string
        int number = jTextField1.getText().length()-1;
        
        String store;
        
        if(length>0)
        {
            StringBuilder backSpace = new StringBuilder(jTextField1.getText());
            backSpace.deleteCharAt(number);   // delete length 1 by 1
            store=backSpace.toString();       // convert 'backSpace' to String
            jTextField1.setText(store);
        }
        
        
    }//GEN-LAST:event_jButton20ActionPerformed

