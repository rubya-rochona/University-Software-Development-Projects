    public void mathematical_operation()
    {
        switch(calculation)
        {
            case 1:         // ADDITION
                result = num + Double.parseDouble(jTextField1.getText());   // converts string to double
                jTextField1.setText(Double.toString(result));               // converts double to string
                break;
                
            case 2:         // SUBTRACTION
                result = num - Double.parseDouble(jTextField1.getText());   
                jTextField1.setText(Double.toString(result));               
                break;
                
            case 3:         // MULTIPLICATION 
                result = num * Double.parseDouble(jTextField1.getText());   
                jTextField1.setText(Double.toString(result));               
                break;
                
            case 4:         // DIVISION 
                result = num / Double.parseDouble(jTextField1.getText());   
                jTextField1.setText(Double.toString(result));               
                break;
                
            case 5:         // DIVISION 
                result = num % Double.parseDouble(jTextField1.getText());   
                jTextField1.setText(Double.toString(result));               
                break;
            
        }
        
        
    }
    
